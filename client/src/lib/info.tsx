export function getInfoCategory(slug: string): InfoCategory | undefined {
  return INFO_CATEGORIES.find((c) => c.slug === slug);
}

export function getInfoDoc(
  categorySlug: string,
  docSlug: string,
): { category: InfoCategory; doc: InfoDoc } | undefined {
  const category = getInfoCategory(categorySlug);
  const doc = category?.docs.find((d) => d.slug === docSlug);
  if (!category || !doc) return undefined;
  return { category, doc };
}
