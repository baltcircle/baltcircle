// Возвращает ответ FAQ по тексту сообщения или null, если ничего не подошло.
export function matchFaq(text: string): FaqEntry | null {
  const t = text.toLowerCase().trim();
  if (!t) return null;
  for (const entry of SUPPORT_FAQ) {
    if (entry.keywords.some((k) => t.includes(k))) return entry;
  }
  return null;
}

// true, если пользователь просит живого оператора.
export function wantsOperator(text: string): boolean {
  const t = text.toLowerCase();
  return OPERATOR_KEYWORDS.some((k) => t.includes(k));
}

